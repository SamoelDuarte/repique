    <h1>Política de Privacidade - Aplicativo Repique</h1>

    <p>O Aplicativo Repique é dedicado a ajudar restaurantes a gerenciar as gorjetas de seus funcionários. A privacidade dos nossos usuários é importante para nós. Esta política de privacidade descreve como coletamos, usamos, divulgamos e protegemos as informações pessoais dos usuários.</p>

    <h2>1. Informações Coletadas</h2>
    <p>O Aplicativo Repique coleta as seguintes informações:</p>
    <ul>
        <li>Informações de contato, como nome, endereço de e-mail e número de telefone.</li>
        <li>Informações de pagamento necessárias para processar os repiques, como números de conta bancária ou informações de PayPal.</li>
    </ul>

    <h2>2. Uso das Informações</h2>
    <p>As informações coletadas são utilizadas exclusivamente para os seguintes propósitos:</p>
    <ul>
        <li>Facilitar o processo de repique das gorjetas dos funcionários do restaurante.</li>
        <li>Comunicar-se com os usuários sobre transações, atualizações do aplicativo ou outras informações relevantes.</li>
    </ul>

    <h2>3. Divulgação a Terceiros</h2>
    <p>O Aplicativo Repique não divulga informações pessoais dos usuários a terceiros, exceto quando exigido por lei ou quando necessário para processar transações.</p>

    <h2>4. Segurança das Informações</h2>
    <p>Implementamos medidas de segurança para proteger as informações pessoais dos usuários contra acesso não autorizado, alteração, divulgação ou destruição.</p>

    <h2>5. Alterações na Política de Privacidade</h2>
    <p>Reservamo-nos o direito de modificar esta política de privacidade a qualquer momento. Recomendamos que os usuários revisem periodicamente esta página para se manterem informados sobre nossas práticas de coleta e uso de informações.</p>

    <h2>6. Contato</h2>
    <p>Se você tiver dúvidas sobre esta política de privacidade, entre em contato conosco através do e-mail: exemplo@repiqueapp.com</p>

